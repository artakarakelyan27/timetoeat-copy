// LS — старт «слова»: либо начало строки, либо не-буква.
//      Расходует один символ — это окей для test()/match() семантики.
const LS = '(?:^|[^а-яёa-z0-9])'
// RB — конец «слова»: lookahead, не далее буквы. Lookahead универсален.
const RB = '(?![а-яёa-z0-9])'
// Помощник для кратких записей «целое слово»:
const wb = (stem) => `${LS}${stem}${RB}`

