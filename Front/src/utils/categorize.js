/**
 * Возвращает категорию по имени продукта.
 * Если ничего не подошло — '📦 Прочее'.
 *
 * @param {string} name — имя продукта (любой регистр, любые формы слов)
 * @returns {string}    — одна из категорий из CAT_ORDER
 */
export function categorize(name) {
    const n = (name || '').toLowerCase().trim()
    if (!n) return FALLBACK_CAT
    for (const [re, cat] of CAT_RULES) {
        if (re.test(n)) return cat
    }
    return FALLBACK_CAT
}

/**
 * Безопасный resolve: сначала пытаемся определить по имени,
 * затем доверяем уже сохранённому cat (если он валидный и не «Прочее»),
 * затем эвристика по подстроке cat-строки.
 *
 * Используется в ShoppingListGroup для отображения, чтобы корректно
 * перекрывать устаревшие/ошибочные cat-поля от бэка или старых записей.
 *
 * @param {{ n?: string, cat?: string }} item
 * @returns {string}
 */
// ── СЛОВАРЬ ДЛЯ СВЯЗКИ БЭКЕНДА И ФРОНТЕНДА ───────────────────
// Если регулярки фронтенда не узнали продукт (например, редкая крупа "Маш"), 
// мы полагаемся на категорию бэкенда и переводим её в UI-формат.
const BACKEND_TO_FRONTEND_MAP = {
    'молочные продукты': '🥛 Молочное',
    'мясо и рыба': '🥩 Мясо и птица', // Рыба перехватится регуляркой раньше, остальное - мясо
    'овощи': '🥦 Овощи',
    'фрукты и ягоды': '🍎 Фрукты и ягоды',
    'бакалея': '🌾 Крупы и макароны', // Фолбэк для неизвестной крупы
    'консервы': '🥫 Консервы',
    'специи и соусы': '🧂 Специи и приправы', // Масла перехватятся регуляркой, остальное сюда
    'хлеб и выпечка': '🍞 Хлеб и выпечка',
    'напитки': '🧃 Напитки'
}

/**
 * Безопасный resolve: сначала пытаемся определить по имени (в приоритете, 
 * так как регулярки UI точнее знают разницу между мясом и рыбой),
 * затем доверяем уже сохранённой категории бэкенда.
 *
 * Используется в ShoppingListGroup для отображения и маппинга.
 *
 * @param {{ n?: string, cat?: string }} item
 * @returns {string}
 */
export function resolveCategory(item) {
    // ШАГ 1. По имени (в идеале — по каноническому имени от бэкенда).
    // Если бэкенд нормализовал строку в "яйцо перепелиное" или "куриное филе", 
    // регулярки фронта дадут 100% точную узкую категорию (🥚 Яйца).
    const byName = categorize(item?.n || '')
    if (byName !== FALLBACK_CAT) return byName

    // ШАГ 2. Фолбэк на категорию бэкенда (если регулярки фронта не справились)
    if (item?.cat) {
        const c = item.cat.toLowerCase().trim()

        // Сначала проверяем точное совпадение или подстроку (на случай если бэк уже шлет UI-категории)
        const found = CAT_ORDER.find(x => x.toLowerCase().includes(c))
        if (found) return found

        // Затем используем наш маппинг из широких бэкенд-категорий
        if (BACKEND_TO_FRONTEND_MAP[c]) {
            return BACKEND_TO_FRONTEND_MAP[c]
        }
    }

    return FALLBACK_CAT
}