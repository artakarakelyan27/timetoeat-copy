/**
 * src/utils/categorize.js
 * ─────────────────────────────────────────────────────────────────────
 * Единый источник правды для категоризации продуктов.
 *
 * ИСПРАВЛЕНИЕ КЛЮЧЕВОГО БАГА (FIX-CAT-01):
 *   В прежней реализации использовался \b — word boundary в JavaScript
 *   работает ТОЛЬКО для ASCII-слов: \w = [A-Za-z0-9_], кириллица сюда
 *   не входит. Поэтому /\bяйц/, /\bлук\b/, /\bсыр\b/, /\bсоль\b/,
 *   /\bмёд\b/, /\bсахар/, /\bрис\b/, /\bмасл/ и десятки других
 *   ключевых стемов МОЛЧА ОТКАЗЫВАЛИ — продукты падали в «Прочее»
 *   или в неверную категорию (яйца → мясо из-за «куриные»).
 *
 *   Решение: заменили \b на кириллице-aware границы:
 *     LS  — left  boundary: (?:^|[^а-яёa-z0-9])  — начало строки или не-буква
 *     RB  — right boundary: (?![а-яёa-z0-9])      — далее не идёт буква (lookahead)
 *
 *   Lookbehind НЕ используется — не поддерживается в Safari < 16.4.
 *
 * ДОПОЛНИТЕЛЬНО ИСПРАВЛЕНО:
 *   • Порядок правил: Молочное → Рыба → Мясо («филе сёмги» → рыба,
 *     «филе курицы» → мясо), Масла → Консервы («оливковое масло» ≠
 *     консервы), Сладкое → Фрукты («джем малиновый» → сладкое),
 *     Напитки → Фрукты («сок апельсиновый» → напитки).
 *   • Расширены стемы: «куриц[уы]», «минта» (минтая/минтаю), «манн»
 *     (манная), «трав[ыа]», «помел», «грейпфрут», «геркулес», «фасоль»
 *     без \b, «нут[ау]?» с кириллице-aware right-boundary, и др.
 *   • «Молотый перец» / «Перец молотый» / «Чёрный перец молотый»
 *     корректно идут в Специи через симметричные паттерны.
 *
 * Используется из:
 *   • components/organisms/ShoppingListGroup.vue (отображение)
 *   • views/MenuView.vue (запись cat при addIngsToShop)
 */

