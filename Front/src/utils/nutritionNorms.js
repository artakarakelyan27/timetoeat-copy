/**
 * Проверяет, подходит ли рецепт для batch-cooking
 * (готовится один раз и хранится 4–5 дней в холодильнике).
 *
 * Логика:
 *   1) явный «плохой» паттерн → false
 *   2) категория из «недружественных» → false
 *   3) явный «хороший» паттерн ИЛИ дружественная категория → true
 *   4) иначе — true только если рецепт типа lunch/dinner и не сырое блюдо
 *      (даём benefit of the doubt — пул иначе слишком узкий)
 *
 * @param {Object} recipe
 * @returns {boolean}
 */
export function isBatchSuitable(recipe) {
  if (!recipe) return false

  const cat = String(recipe.category || '').toLowerCase().trim()
  if (BATCH_UNFRIENDLY_CATEGORIES.has(cat)) return false

  const text = recipeBatchText(recipe)
  if (BATCH_PATTERNS.bad.test(text)) return false

  if (BATCH_FRIENDLY_CATEGORIES.has(cat)) return true
  if (BATCH_PATTERNS.good.test(text)) return true

  // Fallback: рецепт обеда/ужина без явных стоп-слов — допускаем.
  // Завтраки и перекусы по умолчанию НЕ batch-friendly.
  const t = recipe.type || recipe.meal_type
  if (t === 'breakfast' || t === 'snack') return false
  if (t === 'lunch' || t === 'dinner') return true

  return false
}

/** Внутренний хелпер — собирает текст рецепта для regex-матчинга. */
function recipeBatchText(recipe) {
  if (!recipe) return ''
  const tags = (recipe.tags || []).join(' ')
  const ings = (recipe.ings || recipe.ingredients || [])
    .map(i => i.n || i.name || '').join(' ')
  return `${recipe.name || ''} ${tags} ${ings} ${recipe.desc || ''} ${recipe.category || ''}`.toLowerCase()
}

/**
 * Унифицированная проверка: включён ли batch-режим в prefs.
 * В коде встречаются ДВА ключа: 'once_week' (онбординг) и 'weekends' (профиль).
 * Оба означают «готовлю один раз на всю неделю».
 */
export function isBatchMode(prefs) {
  const f = prefs?.cookFreq
  return f === 'once_week' || f === 'weekends' || prefs?.isBatchMode === true
}

