def _ensure_macros(r: dict) -> None:
    """Если у рецепта в seed не указаны БЖУ — выводим их из kcal по стандартному
    распределению P:F:C = 15/30/55. Это грубое приближение, но достаточно
    чтобы скоринг proximity на фронте работал не вхолостую (а не считал всё
    от одной и той же ккал). Точные значения для системных рецептов лучше
    проставить вручную или через парсер."""
    kcal = r.get("kcal")
    if not kcal:
        return
    if r.get("proteins") is None:
        r["proteins"] = round(kcal * 0.15 / 4, 1)   # 4 ккал/г
    if r.get("fats") is None:
        r["fats"] = round(kcal * 0.30 / 9, 1)       # 9 ккал/г
    if r.get("carbs") is None:
        r["carbs"] = round(kcal * 0.55 / 4, 1)


def seed():
    db = SessionLocal()
    try:
        existing = db.query(Recipe).count()
        if existing > 0:
            print(f"✓ БД уже содержит {existing} рецептов, пропускаем seed.")
            return

        for r_data in RECIPES:
            ings = r_data.pop("ingredients")
            _ensure_macros(r_data)
            recipe = Recipe(**r_data)
            db.add(recipe)
            db.flush()
            for i, ing in enumerate(ings):
                db.add(Ingredient(recipe_id=recipe.id, order=i, **ing))

        db.commit()
        print(f"✓ Добавлено {len(RECIPES)} рецептов.")
    finally:
        db.close()


if __name__ == "__main__":
    seed()
